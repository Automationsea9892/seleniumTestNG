package pack1;

public class ifelse {

	public static void main(String[] args) {
		
		int x=20;
		 int y=50; 
		 
		 if(x>y) {
			 System.out.println("x is greater than y");
			 
		 }else if(y>x) {
			 System.out.println("y is greater than x");
		 }else if(x==y) {
			 System.out.println("x and y are equal");
		 }
		 
	}
}
